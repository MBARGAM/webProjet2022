    static function cp(){
        $data = self::$cpLocalite;
        $result = array();

        foreach( $data as $key => $val ){
            if(!in_array($val, $result)){
                $result[]  = $val;
            }

        }
        return $result ;
    }

    static function localite($cpTab){
        $localite = self::$cpLocalite;

        foreach($localite as $key => $local ){

            foreach ($cpTab as $cle =>$val){
                $choix = $val->getCp();
                if( $local == $choix){
                    $communeNew = new Commune();
                    $localiteNew = new Localite();

                    $communeNew ->setCommune($key);
                    $communeNew ->setCp($val);
                    $localiteNew->setLocalite($key);
                    $localiteNew->setCp($val);
                    $localiteNew->setCommune($communeNew);
                    $result[] = $localiteNew;
                }
            }
        }
        return $result ;
    }

    static function commune($cpTab){
        $commune = self::$cpLocalite;

        foreach($commune as $key => $local ){

            foreach ($cpTab as $cle =>$val){
