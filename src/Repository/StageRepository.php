class StageRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Stage::class);
    }

    public function add(Stage $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Stage $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }
    public function findAllStages()
    {
        $this->createQueryBuilder('s')
            ->select('s')
            ->orderBy('s.nom', 'ASC')
            ->distinct('TRUE')
            ->getQuery()
            ->getResult();
    }

    public function findStagePrestataire($value)
    {
        $conn = $this->getEntityManager()->getConnection();
        $sql = '
                SELECT * FROM stage s                                                  
                WHERE s.prestataire_id = '.$value.' AND TIMESTAMPDIFF(SECOND, s.fin_affichage , DATE(NOW()))  < 0 
                ORDER BY s.date_creation DESC ';
                $stmt = $conn->prepare($sql);
               $resultSet = $stmt->executeQuery();

        // returns an array of arrays (i.e. a raw data set)
        return $resultSet->fetchAllAssociative();
    }

